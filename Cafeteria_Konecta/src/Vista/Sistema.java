   public void mostrarDatos(){
       String [] titulos = {"ID, ,Nombre, Referencia , Precio, Peso, Categoria, Stock, Fecha de creaci√≥n "};
       
    String [] registros = new String[8];
           
      DefaultTableModel modelo = new DefaultTableModel(null, titulos);
       
       String SQL = "select * from Productos";
       
       try{
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery(SQL);
           
           while (rs.next()){
               registros[0] = rs.getString("idProductos");
               registros[1] = rs.getString("nombreProducto");
               registros[2] = rs.getString("referenciaProducto");
               registros[3] = rs.getString("precioProducto");
               registros[4] = rs.getString("pesoProducto");
               registros[5] = rs.getString("categoriaProducto");
               registros[6] = rs.getString("stockProducto");
               registros[7] = rs.getString("fechaCreacion");
               
               modelo.addRow(registros);        
           
           }
       
       tb_Productos.setModel(modelo);
       
       }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error al mostrar los datos " + e.getMessage());
    }
   
   }     
   
   
   public void eliminarRegistro(){
       int filaSeleccionada = tb_Productos.getSelectedRow();
       
       try {
           String SQL = "delete from Productos where idProductos = ?" + tb_Productos.getValueAt(filaSeleccionada, 0);
           Statement st = con.createStatement();
           
           int n = st.executeUpdate(SQL);
           
           if(n>=0){
               JOptionPane.showMessageDialog(null, "Registro eliminado");          
     
           }         
           
       }       
       catch(Exception e){
        JOptionPane.showMessageDialog(null, "No se elimin√≥ el registro " + e.getMessage());
        }      
   }  
   
